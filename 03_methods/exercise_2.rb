# 2.  What do the following expressions evaluate to?
#
#     1. x = 2
#
#     2. puts x = 2
#
#     3. p name = "Joe"
#
#     4. four = "four"
#
#     5. print something = "nothing"

x = 2 # => 2

puts x = 2 # => nil

p name = "Joe" # => "Joe"

four = "four" # => "four"

print something = "nothing" # => nil